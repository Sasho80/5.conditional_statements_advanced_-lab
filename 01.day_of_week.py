num_for_day = int(input())

if num_for_day == 1:
    print("Monday")
elif num_for_day == 2:
    print("Tuesday")
elif num_for_day == 3:
    print("Wednesday")
elif num_for_day == 4:
    print("Thursday")
elif num_for_day == 5:
    print("Friday")
elif num_for_day == 6:
    print("Saturday")
elif num_for_day == 7:
    print("Sunday")
else:
    print("Error")


